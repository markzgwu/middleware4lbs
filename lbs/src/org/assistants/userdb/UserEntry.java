package org.assistants.userdb;

public final class UserEntry {
	String userid,pathid,lat,lng,info,altitude,daynum,date,time;

	public final String getUserid() {
		return userid;
	}

	public final void setUserid(String userid) {
		this.userid = userid;
	}

	public final String getPathid() {
		return pathid;
	}

	public final void setPathid(String pathid) {
		this.pathid = pathid;
	}

	public final String getLat() {
		return lat;
	}

	public final void setLat(String lat) {
		this.lat = lat;
	}

	public final String getLng() {
		return lng;
	}

	public final void setLng(String lng) {
		this.lng = lng;
	}

	public final String getInfo() {
		return info;
	}

	public final void setInfo(String info) {
		this.info = info;
	}

	public final String getAltitude() {
		return altitude;
	}

	public final void setAltitude(String altitude) {
		this.altitude = altitude;
	}

	public final String getDaynum() {
		return daynum;
	}

	public final void setDaynum(String daynum) {
		this.daynum = daynum;
	}

	public final String getDate() {
		return date;
	}

	public final void setDate(String date) {
		this.date = date;
	}

	public final String getTime() {
		return time;
	}

	public final void setTime(String time) {
		this.time = time;
	}
	
}
