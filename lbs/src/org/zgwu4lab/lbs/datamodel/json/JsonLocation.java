package org.zgwu4lab.lbs.datamodel.json;

public final class JsonLocation {
	String lng;
	String lat;
	public final String getLng() {
		return lng;
	}
	public final void setLng(String lng) {
		this.lng = lng;
	}
	public final String getLat() {
		return lat;
	}
	public final void setLat(String lat) {
		this.lat = lat;
	}
	
}
