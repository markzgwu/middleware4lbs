package org.zgwu4lab.lbsn.cloaking.gowalla;

public final class GowallaEntry {
	String user;
	String time;
	String latitude;
	String longitude;
	String locationid;
	public final String getUser() {
		return user;
	}
	public final void setUser(String user) {
		this.user = user;
	}
	public final String getTime() {
		return time;
	}
	public final void setTime(String time) {
		this.time = time;
	}
	public final String getLatitude() {
		return latitude;
	}
	public final void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public final String getLongitude() {
		return longitude;
	}
	public final void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public final String getLocationid() {
		return locationid;
	}
	public final void setLocationid(String locationid) {
		this.locationid = locationid;
	}
	
}
